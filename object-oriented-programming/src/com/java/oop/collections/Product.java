package com.java.oop.collections;

public class Product {
    int id;
    String name;
    String email;
    char gender;
    long mobileNo;

    public Product(int id, String name, String email, char gender, long mobileNo) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.gender = gender;
        this.mobileNo = mobileNo;
    }
}
